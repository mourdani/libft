#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*str;
	int		i;
	int		a;

	i = 0;
	a = ft_strlen(src);
	if (!(str = malloc(sizeof(char) * (a + 1))))
		return (NULL);
	while (src[i])
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
